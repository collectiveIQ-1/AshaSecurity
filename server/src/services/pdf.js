function isAffirmative(value) {
  if (value === true) return true;
  const s = String(value ?? '').trim().toLowerCase();
  return ['yes', 'true', '1', 'checked', 'selected'].includes(s);
}

function selectionMark(value) {
  return isAffirmative(value) ? '[X]' : '[ ]';
}

function yesNoMark(value, target) {
  const s = String(value ?? '').trim().toLowerCase();
  if (!s) return '[ ]';
  return s === String(target).trim().toLowerCase() ? '[X]' : '[ ]';
}

function holderEnabled(holderKey, clientReg = {}) {
  if (holderKey === 'main') return true;
  if (holderKey === 'joint1') return !!(clientReg?.jointApplicant?.enabled || clientReg?.jointApplicant?.Enabled);
  if (holderKey === 'joint2') return !!(clientReg?.secondJointApplicant?.enabled || clientReg?.secondJointApplicant?.Enabled);
  return false;
}

function selectionCell(value, enabled = true) {
  if (!enabled) return 'N/A';
  return selectionMark(value);
}


