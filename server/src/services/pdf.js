function pickStepData(formData, def) {
  if (!formData || !def) return undefined;
  // Support possible title-cased keys in persisted docs (older versions)
  const candidates = [def.key, titleCase(def.key), def.title];
  for (const k of candidates) {
    if (k in formData) return formData[k];
  }
  return undefined;
}

function resetCursor(doc, indent = 0) {
  doc.x = doc.page.margins.left + indent;
}

function drawSectionCard(doc, title) {
  const x = doc.page.margins.left;
  const w = doc.page.width - doc.page.margins.left - doc.page.margins.right;
  const h = 26;

  ensureSpace(doc, h + 14);
  const y = doc.y;

  doc.save();
  doc.roundedRect(x, y, w, h, 10).fill("#08152f");
  doc.restore();

  doc.save();
  doc.fillColor("#FFFFFF").font("Helvetica-Bold").fontSize(12).text(title, x + 14, y + 7, {
    width: w - 28,
    align: "left",
    lineBreak: false,
  });
  doc.restore();

  doc.y = y + h + 8;
  resetCursor(doc);
}

function drawSubsectionTitle(doc, title, opts = {}) {
  const x = doc.page.margins.left + (opts.indent || 0);
  const w = doc.page.width - doc.page.margins.left - doc.page.margins.right - (opts.indent || 0);
  ensureSpace(doc, 20);
  const y = doc.y;
  doc.save();
  doc.fillColor(opts.color || "#0f172a").font("Helvetica-Bold").fontSize(opts.fontSize || 10).text(title, x, y, {
    width: w,
    align: "left",
  });
  doc.restore();
  doc.y = y + (opts.height || 16);
  resetCursor(doc);
}

function addNumberedRequirement(doc, index, text) {
  ensureSpace(doc, 26);
  resetCursor(doc);
  const x = doc.page.margins.left;
  const y = doc.y;
  doc.save();
  doc.roundedRect(x, y, 18, 18, 6).fillAndStroke("#E2E8F0", "#CBD5E1");
  doc.fillColor("#0f172a").font("Helvetica-Bold").fontSize(9).text(String(index), x, y + 4.5, {
    width: 18,
    align: "center",
  });
  doc.restore();

  doc.fillColor("#334155").font("Helvetica").fontSize(9).text(text, x + 26, y, {
    width: doc.page.width - doc.page.margins.left - doc.page.margins.right - 26,
    align: "left",
  });
  doc.moveDown(0.55);
  resetCursor(doc);
}

