function renderLocalIndividualClientRegistration(doc, data, opts = {}) {
  const v = data || {};
  const shade = { v: false };
  const holders = [
    { key: 'main', label: 'Main', enabled: true },
    { key: 'joint1', label: 'Joint 1', enabled: holderEnabled('joint1', v) },
    { key: 'joint2', label: 'Joint 2', enabled: holderEnabled('joint2', v) },
  ];
  const heading = opts.compact ? drawSubsectionTitle : drawSectionCard;

  const renderPersonBlock = (title, person, enabled = true) => {
    if (!enabled || isEmptyValue(person)) return;
    heading(doc, title);
    Object.entries(person || {}).forEach(([k, val]) => {
      if (k === 'enabled') return;
      if (isEmptyValue(val)) return;
      writeAnyPretty(doc, k, val, 0, shade);
    });
    doc.moveDown(0.25);
  };

  renderPersonBlock('Principal Applicant', v.principal || {});
  renderUploadedSignatureCard(doc, opts.files, 'principalSig', 'Principal Applicant Signature', { omitPreviewImages: true });
  renderPersonBlock('Joint Applicant', v.jointApplicant || {}, holders[1].enabled);
  renderPersonBlock('Second Joint Applicant', v.secondJointApplicant || {}, holders[2].enabled);
  renderPersonBlock('Investment Decision', v.investmentDecision || {});
  renderPersonBlock('Staff Declaration', v.staffDeclaration || {});
  renderPersonBlock('Office Use', v.officeUse || {});

  const kyc = v.kycProfile || {};
  if (opts.includeKyc !== false) {
    renderIndividualKycProfile(doc, kyc, holders, shade, opts.kycOptions || {});
  }
}


