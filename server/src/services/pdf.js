function buildPersonName(person) {
  const p = person || {};
  const title = prefer(p.title, p.Title);
  const initials = prefer(p.name, p.Name, p.namesByInitials, p["Names By Initials"], p.initials, p.Initials);
  const surname = prefer(p.surname, p.Surname, p.surnames, p.Surnames);

  const parts = [title, initials, surname]
    .map((s) => String(s ?? "").trim())
    .filter(Boolean);

  return parts.join(" ");
}

function buildPersonAddress(person) {
  const p = person || {};
  return prefer(
    p.permanentAddress,
    p["Permanent Address"],
    p.correspondenceAddress,
    p["Correspondence Address"],
    p.residentialAddress,
    p["Residential Address"],
    p.officeAddress,
    p["Office Address"]
  );
}

function normalizeFormDataForPdf(formKey, formData) {
  const out = cloneJson(formData || {});

  // Fix the known Local Individual mismatches between UI and PDF:
  // 1) "Declaration" contains staff/authorized/office-use fields but UI treats them as part of Client Registration.
  // 2) UI captures Parties fields inside Client Registration, not inside step.parties arrays.
  if (String(formKey || "") === "local_individual") {
    const cr = out.clientRegistration || {};
    const decl = out.declaration || {};

    // Move office-use fields from Declaration -> Client Registration (PDF only)
    const moved = {
      staffName: decl?.staffName,
      authorizedPersonsName: decl?.authorizedPersonsName,
      authorizedPersonsNic: decl?.authorizedPersonsNic,
      officeUseOnly: decl?.officeUseOnly,
    };

    // Put them under a clear group so they don't appear under Declaration in the PDF.
    cr.officeUse = {
      staffName: moved.staffName,
      authorizedPersonsName: moved.authorizedPersonsName,
      authorizedPersonsNic: moved.authorizedPersonsNic,
      ...(isObject(moved.officeUseOnly) ? moved.officeUseOnly : {}),
    };

    // Remove from Declaration to prevent wrong-step display
    if (isObject(decl)) {
      delete decl.staffName;
      delete decl.authorizedPersonsName;
      delete decl.authorizedPersonsNic;
      delete decl.officeUseOnly;
    }

    // Build parties (Principal / Joint1 / Joint2) from Client Registration
    const principal = cr?.principal || {};
    const joint1 = cr?.jointApplicant || {};
    const joint2 = cr?.secondJointApplicant || {};

    const party1 = {
      name: buildPersonName(principal),
      nicNo: prefer(principal?.nic, principal?.Nic),
      address: buildPersonAddress(principal),
    };
    const party2 = (joint1?.enabled || joint1?.Enabled)
      ? {
          name: buildPersonName(joint1),
          nicNo: prefer(joint1?.nic, joint1?.Nic),
          address: buildPersonAddress(joint1),
        }
      : { name: "", nicNo: "", address: "" };
    const party3 = (joint2?.enabled || joint2?.Enabled)
      ? {
          name: buildPersonName(joint2),
          nicNo: prefer(joint2?.nic, joint2?.Nic),
          address: buildPersonAddress(joint2),
        }
      : { name: "", nicNo: "", address: "" };

    // Ensure Local Individual agreement-style sections always exist as objects so
    // the PDF renderer can print the full agreement wording even when the UI did
    // not persist a dedicated payload for that step.
    if (!isObject(out.clientAgreement)) out.clientAgreement = {};
    if (!isObject(out.creditFacility)) out.creditFacility = {};
    if (!isObject(out.schedule1)) out.schedule1 = {};
    if (!isObject(out.schedule2)) out.schedule2 = {};

    const declarationSchedule2 = isObject(decl?.schedule2) ? decl.schedule2 : {};
    out.schedule2 = {
      ...declarationSchedule2,
      ...out.schedule2,
      party1Name: prefer(out.schedule2?.party1Name, declarationSchedule2?.party1Name, declarationSchedule2?.person1?.name, party1.name),
      party1Id: prefer(out.schedule2?.party1Id, declarationSchedule2?.party1Id, declarationSchedule2?.person1?.nicNo, party1.nicNo),
      party1Address: prefer(out.schedule2?.party1Address, declarationSchedule2?.party1Address, declarationSchedule2?.person1?.address, party1.address),
      party2Name: prefer(out.schedule2?.party2Name, declarationSchedule2?.party2Name, declarationSchedule2?.person2?.name, party2.name),
      party2Id: prefer(out.schedule2?.party2Id, declarationSchedule2?.party2Id, declarationSchedule2?.person2?.nicNo, party2.nicNo),
      party2Address: prefer(out.schedule2?.party2Address, declarationSchedule2?.party2Address, declarationSchedule2?.person2?.address, party2.address),
      party3Name: prefer(out.schedule2?.party3Name, declarationSchedule2?.party3Name, declarationSchedule2?.person3?.name, party3.name),
      party3Id: prefer(out.schedule2?.party3Id, declarationSchedule2?.party3Id, declarationSchedule2?.person3?.nicNo, party3.nicNo),
      party3Address: prefer(out.schedule2?.party3Address, declarationSchedule2?.party3Address, declarationSchedule2?.person3?.address, party3.address),
      explainedByName: prefer(out.schedule2?.explainedByName, declarationSchedule2?.explainedByName, declarationSchedule2?.explainedBy),
      date: prefer(out.schedule2?.date, declarationSchedule2?.date),
    };

    // Overwrite / backfill parties arrays because the UI does not write to them;
    // otherwise PDFs show blanks or a dash instead of the full agreement section.
    ["clientAgreement", "creditFacility", "paymentInstruction", "directionOnline"].forEach((k) => {
      const step = out?.[k];
      if (!isObject(step)) return;
      step.parties = [party1, party2, party3];
      out[k] = step;
    });

    out.clientRegistration = cr;
    out.declaration = decl;
  }

  return out;
}

