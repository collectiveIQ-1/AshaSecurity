function titleCase(s) {
  return String(s || "")
    .replace(/([A-Z])/g, " $1")
    .replace(/_/g, " ")
    .replace(/\s+/g, " ")
    .trim()
    .replace(/^./, (c) => c.toUpperCase());
}

function isObject(v) {
  return v && typeof v === "object" && !Array.isArray(v);
}

function formatValue(value) {
  if (value === null || value === undefined || value === "") return "-";
  if (typeof value === "boolean") return value ? "Yes" : "No";
  return String(value);
}


function isEmptyValue(value) {
  if (value === null || value === undefined) return true;
  if (typeof value === "string" && value.trim() === "") return true;
  if (Array.isArray(value)) return value.length === 0 || value.every(isEmptyValue);
  if (isObject(value)) return Object.values(value).every(isEmptyValue);
  return false;
}

function ensureSpace(doc, neededHeight) {
  const bottom = doc.page.height - doc.page.margins.bottom;
  if (doc.y + neededHeight > bottom) {
    doc.addPage();
    return true;
  }
  return false;
}

function findUploadedFile(files, field) {
  return (Array.isArray(files) ? files : []).find((file) => String(file?.field || "") === String(field || ""));
}

function renderUploadedSignatureCard(doc, files, field, title = "Principal Applicant Signature", opts = {}) {
  const file = findUploadedFile(files, field);
  if (!file?.path) return;

  if (opts?.omitPreviewImages !== false) {
    return;
  }

  const localPath = path.resolve(process.cwd(), `.${file.path}`);
  if (!fs.existsSync(localPath)) return;

  ensureSpace(doc, 150);
  doc.save();
  const x = doc.page.margins.left;
  const y = doc.y;
  const w = doc.page.width - doc.page.margins.left - doc.page.margins.right;
  const h = 118;

  doc.roundedRect(x, y, w, h, 14).fillAndStroke("#F8FAFC", "#CBD5E1");
  doc.fillColor("#64748B").font("Helvetica-Bold").fontSize(8).text("PREVIEW", x + 14, y + 12, { width: w - 28 });
  doc.fillColor("#0F172A").font("Helvetica-Bold").fontSize(12).text(title, x + 14, y + 26, { width: w - 28 });
  doc.fillColor("#475569").font("Helvetica").fontSize(8.5).text("Uploaded signature mirrored here for quick review.", x + 14, y + 42, { width: w - 28 });

  const imageBoxX = x + 14;
  const imageBoxY = y + 58;
  const imageBoxW = w - 28;
  const imageBoxH = 44;
  doc.roundedRect(imageBoxX, imageBoxY, imageBoxW, imageBoxH, 10).fillAndStroke("#FFFFFF", "#E2E8F0");
  try {
    doc.image(localPath, imageBoxX + 8, imageBoxY + 6, {
      fit: [imageBoxW - 16, imageBoxH - 12],
      align: "center",
      valign: "center",
    });
  } catch {
    doc.fillColor("#64748B").font("Helvetica").fontSize(8.5).text(file.originalName || "Signature uploaded", imageBoxX + 10, imageBoxY + 15, {
      width: imageBoxW - 20,
      align: "center",
    });
  }
  doc.restore();
  doc.y = y + h + 8;
}

function drawHeader(doc, meta) {
  const { title, subtitleLeft, subtitleRight } = meta;
  const x = doc.page.margins.left;
  const y = doc.page.margins.top;
  const w = doc.page.width - doc.page.margins.left - doc.page.margins.right;

  // Top accent bar
  doc.save();
  doc.rect(x, y - 25, w, 8).fill("#0f172a");
  doc.restore();

  doc.fillColor("#0f172a").font("Helvetica-Bold").fontSize(18).text(title, x, y, {
    width: w,
    align: "left",
  });

  doc.moveDown(0.35);

  doc.fillColor("#334155").font("Helvetica").fontSize(10);
  doc.text(subtitleLeft || "", { width: w, continued: true });
  doc.text(subtitleRight || "", { width: w, align: "right" });

  doc.moveDown(0.8);
  doc.moveTo(x, doc.y).lineTo(x + w, doc.y).strokeColor("#CBD5E1").stroke();
  doc.moveDown(0.8);
}

function drawFooter(doc, pageNum) {
  const x = doc.page.margins.left;
  const w = doc.page.width - doc.page.margins.left - doc.page.margins.right;
  // IMPORTANT: PDFKit will create a *new page* if we try to draw below the
  // content area bottom boundary (page.height - margins.bottom).
  // Keep the footer safely inside the content area.
  const y = doc.page.height - doc.page.margins.bottom - 14;

  doc.save();
  doc.fillColor("#64748B").font("Helvetica").fontSize(9);
  doc.text("Generated by SmartPortal", x, y, { width: w, align: "left" });
  doc.text(`Page ${pageNum}`, x, y, { width: w, align: "right" });
  doc.restore();
}

function drawStepHeader(doc, stepIndex, title) {
  resetCursor(doc);
  const x = doc.page.margins.left;
  const w = doc.page.width - doc.page.margins.left - doc.page.margins.right;

  ensureSpace(doc, 46);

  const y = doc.y;

  // Dark rounded bar
  doc.save();
  doc.roundedRect(x, y, w, 30, 8).fill("#0f172a");
  doc.restore();

  doc.save();
  doc.fillColor("#E2E8F0").font("Helvetica-Bold").fontSize(10);
  doc.text(`STEP ${stepIndex}`, x + 12, y + 8, { width: 70 });
  doc.fillColor("#FFFFFF").font("Helvetica-Bold").fontSize(12);
  doc.text(title, x + 82, y + 7, { width: w - 92 });
  doc.restore();

  doc.y = y + 38;
  resetCursor(doc);
}


function addParagraph(doc, text, opts = {}) {
  if (isEmptyValue(text)) return;
  ensureSpace(doc, opts.neededHeight || 18);
  const indent = opts.indent || 0;
  resetCursor(doc, indent);
  doc.fillColor(opts.color || "#334155").font(opts.bold ? "Helvetica-Bold" : "Helvetica").fontSize(opts.fontSize || 9).text(String(text), doc.page.margins.left + indent, doc.y, {
    width: doc.page.width - doc.page.margins.left - doc.page.margins.right - indent,
    align: opts.align || "left",
  });
  doc.moveDown(opts.gap ?? 0.35);
  resetCursor(doc);
}

function addParagraphList(doc, items = [], opts = {}) {
  (items || []).forEach((item) => {
    if (typeof item === "string") {
      addParagraph(doc, item, opts);
      return;
    }
    if (!item || typeof item !== "object") return;
    if (item.type === "heading") {
      addParagraph(doc, item.text, { ...opts, bold: true, fontSize: 10, gap: 0.2, color: "#0f172a" });
      return;
    }
    if (item.type === "bullet") {
      addIndentedBullet(doc, item.text, { indent: item.indent || 14 });
      return;
    }
    addParagraph(doc, item.text || "", { ...opts, bold: !!item.bold, fontSize: item.fontSize || opts.fontSize || 9, indent: item.indent || opts.indent || 0 });
  });
}

function renderAgreementParties(doc, parties = [], date = {}, opts = {}) {
  const day = prefer(date?.day, date?.date, date?.dd);
  const month = prefer(date?.month, date?.mm);
  const year = prefer(date?.year, date?.yyyy);
  const dateText = [day, month, year].filter(Boolean).join(" / ") || "-";

  addParagraph(doc, `${opts.introPrefix || "This Agreement is made and entered into on"} ${dateText}.`, { fontSize: 10 });
  addParagraph(doc, opts.betweenText || "By and between:", { bold: true, fontSize: 10, gap: 0.25, color: "#0f172a" });

  parties.forEach((party, idx) => {
    const entry = party || {};
    const heading = entry.label || `Party (${idx + 1})`;
    (opts.headingRenderer || drawSectionCard)(doc, heading);
    const shade = { v: false };
    addKeyValueRow(doc, "Name", prefer(entry.name, entry.fullName, entry.clientName), { shade: (shade.v = !shade.v) });
    addKeyValueRow(doc, opts.idLabel || "National Identity Card / Registration No.", prefer(entry.idNo, entry.nicNo, entry.nicCds, entry.registrationNo), { shade: (shade.v = !shade.v) });
    addKeyValueRow(doc, "Address", prefer(entry.address, entry.registeredAddress, entry.permanentAddress, entry.correspondenceAddress), { shade: (shade.v = !shade.v) });
    doc.moveDown(0.15);
  });
}

